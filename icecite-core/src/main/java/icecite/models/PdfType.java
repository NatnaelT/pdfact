package icecite.models;

/**
 * An enumeration of types for PDF elements.
 * 
 * @author Claudius Korzen
 */
public enum PdfType {
  /**
   * Characters.
   */
  CHARACTERS,
  /**
   * Words.
   */
  WORDS,
  /**
   * Text lines.
   */
  TEXTLINES,
  /**
   * Paragraphs.
   */
  PARAGRAPHS,
  /**
   * Figures.
   */
  FIGURES,
  /**
   * Shapes.
   */
  SHAPES
}
